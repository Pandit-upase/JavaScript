// Demonstrating the use of 'typeof' operator in JavaScript

// String
typeofString = "Hello, World!";
console.log("Type of typeofString:", typeof typeofString);

// Number
typeofNumber = 42;
console.log("Type of typeofNumber:", typeof typeofNumber);

// Boolean
typeofBoolean = true;
console.log("Type of typeofBoolean:", typeof typeofBoolean);

// Undefined
typeofUndefined = undefined;
console.log("Type of typeofUndefined:", typeof typeofUndefined);

// Null
typeofNull = null;
console.log("Type of typeofNull:", typeof typeofNull);

// Object
typeofObject = { name: "John", age: 30 };
console.log("Type of typeofObject:", typeof typeofObject);

// Array
typeofArray = [1, 2, 3, 4, 5];
console.log("Type of typeofArray:", typeof typeofArray);

// Function
typeofFunction = function() { return "Hello!"; };
console.log("Type of typeofFunction:", typeof typeofFunction);

// Symbol
typeofSymbol = Symbol("id");
console.log("Type of typeofSymbol:", typeof typeofSymbol);